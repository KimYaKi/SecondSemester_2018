package user;

public class Car {
	private String modelID;
	private String manufacturer;
	private String modelName;
	private int modelYear;
	private int modelDsp;
	private String modelType;
	private String modelEngine;
	private String modelGear;
	private int gearNumber;
	private int modelMax;
	private String modelDriving;
	private String modelFuel;
	
	public Car() {
		modelID=null;
	}
	
	public String getModelID() {
		return modelID;
	}
	public void setModelID(String modelID) {
		this.modelID = modelID;
	}
	public String getManufacturer() {
		return manufacturer;
	}
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	public String getModelName() {
		return modelName;
	}
	public void setModelName(String modelName) {
		this.modelName = modelName;
	}
	public int getModelYear() {
		return modelYear;
	}
	public void setModelYear(int modelYear) {
		this.modelYear = modelYear;
	}
	public int getModelDsp() {
		return modelDsp;
	}
	public void setModelDsp(int modelDsp) {
		this.modelDsp = modelDsp;
	}
	public String getModelType() {
		return modelType;
	}
	public void setModelType(String modelType) {
		this.modelType = modelType;
	}
	public String getModelEngine() {
		return modelEngine;
	}
	public void setModelEngine(String modelEngine) {
		this.modelEngine = modelEngine;
	}
	public String getModelGear() {
		return modelGear;
	}
	public void setModelGear(String modelGear) {
		this.modelGear = modelGear;
	}
	public int getGearNumber() {
		return gearNumber;
	}
	public void setGearNumber(int gearNumber) {
		this.gearNumber = gearNumber;
	}
	public int getModelMax() {
		return modelMax;
	}
	public void setModelMax(int modelMax) {
		this.modelMax = modelMax;
	}
	public String getModelDriving() {
		return modelDriving;
	}
	public void setModelDriving(String modelDriving) {
		this.modelDriving = modelDriving;
	}
	public String getModelFuel() {
		return modelFuel;
	}
	public void setModelFuel(String modelFuel) {
		this.modelFuel = modelFuel;
	}
	
}
